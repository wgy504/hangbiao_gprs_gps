INT8U Get_Rhythm_Index(void)
{
    INT8U retVal=0;
    retVal=RhythmPort;
	
	
    return 0xff-retVal;
	
    /* RhythmCS0~4 -- PB.5~PB.9*/
    /* RhythmCS5~7 -- PC.13~PC.15*/
}

INT8U Get_Switch_Index(void)
{
 	INT8U retVal=0,retVal_H=0;
	retVal=(0x03&(SwitchPort>>6));

        retVal_H=(0x0C&(SwitchPort>>1));
	return (0x0F-retVal-retVal_H);
	
	/* RhythmCS0~4 -- PB.5~PB.9*/
	/* RhythmCS5~7 -- PC.13~PC.15*/
}


/*
根据当前是白天或者晚上，结合当前的光强，计算是否需要开灯
假定：开关灯为300lux，正负100lux
白天：nCurrVal>400
晚上：nCurrVal<200
中间阶段为过渡阶段
光强由弱到强（由晚上到白天）：
*/

void Flash_Ctrl(BOOL Flash_En_Flag,unsigned char Flash_Rhythm_Index)
{
  static INT16U Curr_Tick=0;
  static INT8U Flash_Ctrl_Stage=0;
  static BOOL Last_Light_On_Flag=FALSE;
  
  static BOOL bFlag=FALSE;
  
  if(Flash_En_Flag==FALSE)//关灯
  {
    Flash_Ctrl_Stage=0;
    Curr_Tick=0;
    FLASH_OUT_EN(FALSE);
    return;
  }
// //1  2  3--Flash_Period_Max_Count
// //01 23 45--Flash_Ctrl_Stage
// //12 34 56--Time
// //10 10	 --LED


  if(Curr_Tick==0)
  {
    //Curr_Tick=Flash_Rhythm_Chat[Flash_Rhythm_Index][Flash_Ctrl_Stage+2];
    Light_On_Flag=(Flash_Rhythm_Chat[Flash_Rhythm_Index][Flash_Ctrl_Stage+2]==0)?Last_Light_On_Flag:((Flash_Ctrl_Stage+1)%2);	
    Curr_Tick+=4;//因为是4mS中断一次

    if(Flash_Ctrl_Stage==0)//
    {
      bFlag=!bFlag;
      FLS_A_EN(bFlag);
      FLS_B_EN(!bFlag);
      
//////      if((DevInfo.FlsDebugLevel&DBG_MSG))//
//////      {
//////        printf("MSG-FLS:bFlag=%d.Rhythm=0x%02X(%d)\r\n\r\n",
//////               bFlag,Flash_Rhythm_Index,Flash_Rhythm_Index);
//////      }
    }
    
    if((DevInfo.FlsDebugLevel&DBG_MSG))//
    {
      printf("MSG-FLS:bFlag=%d.Light_On_Flag=%d,Flash_Ctrl_Stage=%d,DevInfo.Rhythm=%d.\r\n\r\n",
             bFlag,Light_On_Flag,Flash_Ctrl_Stage,DevInfo.Rhythm);
    }
    
  }
  else
  {
    Last_Light_On_Flag=Light_On_Flag;
    if(Curr_Tick>=Flash_Rhythm_Chat[Flash_Rhythm_Index][Flash_Ctrl_Stage+2])//时间到，切换到下一阶段
    {
      Curr_Tick=0;			
      if(++Flash_Ctrl_Stage>=Flash_Rhythm_Chat[Flash_Rhythm_Index][0]*2)
      {
        Flash_Ctrl_Stage=0;
        
        if((DevInfo.FlsDebugLevel&DBG_MSG))//
        {
          printf("MSG-FLS:Flash_Rhythm_Index=%d,Flash_Rhythm_Chat[Flash_Rhythm_Index][0]=%d.\r\n\r\n",
                 Flash_Rhythm_Index,Flash_Rhythm_Chat[Flash_Rhythm_Index][0]);
        }
        
      }			
    }
    else
    {
      Curr_Tick+=4;
    }
  }
  FLASH_OUT_EN(Light_On_Flag);
}
